''' 1. Write a Python program to find the number of times 4 appears in the tuple.
'''

# Define the tuple
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Use the count() method to find the number of times 4 appears
count_of_four = tuplex.count(4)

# Print the result
print("Number of times 4 appears in the tuple:", count_of_four)

''' output
Number of times 4 appears in the tuple: 3
'''

'''
1. Sum all the items in a list:
'''
# List of numbers
numbers = [1, 2, 3, 4, 5]

# Variable to store the sum
total = 0  

# Loop through each number and add to total
for num in numbers:
    total += num

# Display the sum
print("Sum of all items in the list:", total)

''' output 
Sum of all items in the list: 15
'''
